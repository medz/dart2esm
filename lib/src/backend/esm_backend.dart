  String _emitInstanceGet(k.InstanceGet expression) {
    final name = expression.name.text;
    if (name == 'iterator') {
      _usedHelpers.add('__dartIterator');
      return '__dartIterator(${emitExpression(expression.receiver)})';
    }
    final receiver = emitExpression(expression.receiver);
    if (name == 'first' &&
        _isAsyncStreamMember(expression.interfaceTargetReference, name)) {
      _usedHelpers.add('__dartStream');
      return '__dartStreamFirst($receiver)';
    }
    if (name == 'lengthInBytes' &&
        _isTypedDataMember(expression.interfaceTargetReference, name)) {
      return '$receiver.byteLength';
    }
    if (name == 'isEmpty' &&
        _isCoreMember(expression.interfaceTargetReference, 'String', name)) {
      return '$receiver.length === 0';
    }
    if (name == 'isNotEmpty' &&
        _isCoreMember(expression.interfaceTargetReference, 'String', name)) {
      return '$receiver.length !== 0';
    }
    if (name == 'codeUnits' &&
        _isCoreMember(expression.interfaceTargetReference, 'String', name)) {
      return 'Array.from({ length: $receiver.length }, (_, index) => $receiver.charCodeAt(index))';
    }
    if (name == 'isOdd' &&
        _isCoreMember(expression.interfaceTargetReference, 'int', name)) {
      return '(Math.trunc($receiver) % 2 !== 0)';
    }
    if (name == 'isEven' &&
        _isCoreMember(expression.interfaceTargetReference, 'int', name)) {
      return '(Math.trunc($receiver) % 2 === 0)';
    }
    final numberGet = _emitNumberInstanceGet(
      expression.interfaceTargetReference,
      name,
      receiver,
    );
    if (numberGet != null) {
      return numberGet;
    }
    if (_isCoreMember(expression.interfaceTargetReference, 'BigInt', name)) {
      final bigIntGet = switch (name) {
        'isEven' => '($receiver % 2n === 0n)',
        'isOdd' => '($receiver % 2n !== 0n)',
        'isNegative' => '($receiver < 0n)',
        'sign' => '($receiver < 0n ? -1 : ($receiver > 0n ? 1 : 0))',
        'bitLength' => () {
          _usedHelpers.add('__dartBigIntBitLength');
          return '__dartBigIntBitLength($receiver)';
        }(),
        _ => null,
      };
      if (bigIntGet != null) {
        return bigIntGet;
      }
    }
    if (name == 'offsetInBytes' &&
        _isTypedDataMember(expression.interfaceTargetReference, name)) {
      return '$receiver.byteOffset';
    }
    if (name == 'buffer' &&
        _isTypedDataMember(expression.interfaceTargetReference, name)) {
      return '$receiver.buffer';
    }
    if (name == 'elementSizeInBytes' &&
        _isTypedDataMember(expression.interfaceTargetReference, name)) {
      return _typedDataElementSizeExpression(receiver);
    }
    final receiverCollectionKind = _expressionCollectionKind(
      expression.receiver,
    );
    if (name == 'isEmpty' &&
        _isCoreCollectionMember(expression.interfaceTargetReference, name)) {
      if (_isCoreMapMember(expression.interfaceTargetReference, name) ||
          _isCoreSetMember(expression.interfaceTargetReference, name) ||
          receiverCollectionKind == 'Map' ||
          receiverCollectionKind == 'Set') {
        return '$receiver.size === 0';
      }
      return '$receiver.length === 0';
    }
    if (name == 'isNotEmpty' &&
        _isCoreCollectionMember(expression.interfaceTargetReference, name)) {
      if (_isCoreMapMember(expression.interfaceTargetReference, name) ||
          _isCoreSetMember(expression.interfaceTargetReference, name) ||
          receiverCollectionKind == 'Map' ||
          receiverCollectionKind == 'Set') {
        return '$receiver.size !== 0';
      }
      return '$receiver.length !== 0';
    }
    if (name == 'first' &&
        _isCoreCollectionMember(expression.interfaceTargetReference, name)) {
      if (_isCoreListMember(expression.interfaceTargetReference, name) ||
          receiverCollectionKind == 'List') {
        return '$receiver[0]';
      }
      _usedHelpers.add('__dartIterableFirst');
      return '__dartIterableFirst($receiver)';
    }
    if (name == 'last' &&
        _isCoreCollectionMember(expression.interfaceTargetReference, name)) {
      if (_isCoreListMember(expression.interfaceTargetReference, name) ||
          receiverCollectionKind == 'List') {
        return '$receiver[$receiver.length - 1]';
      }
      _usedHelpers.add('__dartIterableLast');
      return '__dartIterableLast($receiver)';
    }
    if (name == 'reversed' &&
        _isCoreCollectionMember(expression.interfaceTargetReference, name)) {
      return 'Array.from($receiver).reverse()';
    }
    if (name == 'length' &&
        _isCoreCollectionMember(expression.interfaceTargetReference, name)) {
      if (_isCoreSetMember(expression.interfaceTargetReference, name) ||
          _isCoreMapMember(expression.interfaceTargetReference, name) ||
          receiverCollectionKind == 'Set' ||
          receiverCollectionKind == 'Map') {
        return '$receiver.size';
      }
      return '$receiver.length';
    }
    if (name == 'keys' &&
        _isCoreMember(expression.interfaceTargetReference, 'Map', 'keys')) {
      return '$receiver.keys()';
    }
    if (name == 'values' &&
        _isCoreMember(expression.interfaceTargetReference, 'Map', 'values')) {
      return '$receiver.values()';
    }
    if (name == 'entries' &&
        _isCoreMember(expression.interfaceTargetReference, 'Map', 'entries')) {
      return 'Array.from($receiver, ([key, value]) => ({ key, value }))';
    }
    return _emitPropertyGet(receiver, _memberName(name));
  }

