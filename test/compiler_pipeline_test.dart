void main() {
  test('compiler core exposes the ordered stage contract', () {
    expect(
      const Dart2EsmPipelineOptions(runMain: true).allowLegacyOracle,
      isFalse,
    );
    expect(
      dart2EsmCompilerStageOrder,
      dart2EsmCompilerStageContracts
          .map((contract) => contract.stageId)
          .toList(),
    );
    expect(dart2EsmCompilerStageContracts.map((contract) => contract.stageId), [
      Dart2EsmCompilerStageId.kernelFrontend,
      Dart2EsmCompilerStageId.semanticWorld,
      Dart2EsmCompilerStageId.dartLowering,
      Dart2EsmCompilerStageId.esmIrBuilder,
      Dart2EsmCompilerStageId.moduleNormalizer,
      Dart2EsmCompilerStageId.runtimeLinker,
      Dart2EsmCompilerStageId.esmCodegen,
    ]);
    expect(const KernelFrontendStage().stageId, dart2EsmCompilerStageOrder[0]);
    expect(const SemanticWorldStage().stageId, dart2EsmCompilerStageOrder[1]);
    expect(
      const KernelToEsmIrLoweringStage().stageId,
      dart2EsmCompilerStageOrder[2],
    );
    expect(const EsmIrBuilderStage().stageId, dart2EsmCompilerStageOrder[3]);
    expect(
      const ModuleNormalizerStage().stageId,
      dart2EsmCompilerStageOrder[4],
    );
    expect(const RuntimeLinkerStage().stageId, dart2EsmCompilerStageOrder[5]);
    expect(const EsmCodegenStage().stageId, dart2EsmCompilerStageOrder[6]);
    expect(
      dart2EsmStageContractFor(Dart2EsmCompilerStageId.kernelFrontend).output,
      'KernelFrontendResult',
    );
    expect(
      dart2EsmStageContractFor(
        Dart2EsmCompilerStageId.dartLowering,
      ).allowsRuntimeHelperReference,
      isTrue,
    );
    expect(
      dart2EsmStageContractFor(
        Dart2EsmCompilerStageId.esmCodegen,
      ).allowsKernelAccess,
      isFalse,
    );
  });

  test('compiles supported Kernel through the new compiler core', () {
    final libraryUri = Uri.parse('package:sample/main.dart');
    final library = k.Library(libraryUri, fileUri: libraryUri);
    final helperParameter = k.VariableDeclaration('value');
    final helper = _procedure(
      'helper',
      body: k.Block([k.ReturnStatement(k.VariableGet(helperParameter))]),
      positionalParameters: [helperParameter],
    );
    final message = k.VariableDeclaration(
      'message',
      initializer: k.StringLiteral('ok'),
      isFinal: true,
    );
    final main = _procedure(
      'main',
      body: k.Block([
        message,
        k.ExpressionStatement(
          k.StaticInvocation(helper, k.Arguments([k.VariableGet(message)])),
        ),
      ]),
    );
    library.addProcedure(helper);
    library.addProcedure(main);
    final component = k.Component(libraries: [library]);
    component.setMainMethodAndMode(main.reference, true);

    final result = Dart2EsmCompilerPipeline(
      options: const Dart2EsmPipelineOptions(runMain: true),
    ).compile(component);

    expect(result.path, Dart2EsmCompilerPath.newCore);
    expect(result.completedStages, dart2EsmCompilerStageOrder);
    expect(result.usedLegacyOracle, isFalse);
    expect(result.kernel.component, same(component));
    expect(result.semantic?.world.main, same(main));
    expect(result.lowering?.items, hasLength(3));
    expect(result.irBuild?.module.items, hasLength(3));
    expect(result.lowering?.runtimeHelpers, isEmpty);
    expect(result.normalization?.irBuild, same(result.irBuild));
    expect(result.normalization?.invalidatesSemanticWorld, isFalse);
    expect(result.runtime?.linkedHelpers, isEmpty);
    expect(result.codegen?.code, same(result.code));
    expect(result.code, '''
// Generated by dart2esm.

export function helper(value) {
  return value;
}

export function main() {
  const message = "ok";
  helper(message);
}

main();
''');
  });

  test('codegen parenthesizes expression callees', () {
    const module = EsmModuleIr(
      items: [
        EsmExpressionStatementIr(
          EsmCallIr(
            callee: EsmArrowFunctionIr(
              parameters: ['value'],
              body: EsmIdentifierIr('value'),
            ),
            arguments: [EsmStringLiteralIr('ok')],
          ),
        ),
      ],
    );

    expect(const EsmCodegenStage().emit(module).code, '''
// Generated by dart2esm.

((value) => value)("ok");
''');
  });

  test('codegen parenthesizes arrow functions in nullish coalescing', () {
    const module = EsmModuleIr(
      items: [
        EsmExpressionStatementIr(
          EsmNullishCoalesceIr(
            left: EsmIdentifierIr('compare'),
            right: EsmArrowFunctionIr(
              parameters: ['left', 'right'],
              body: EsmNumberLiteralIr(0),
            ),
          ),
        ),
      ],
    );

    expect(const EsmCodegenStage().emit(module).code, '''
// Generated by dart2esm.

(compare ?? ((left, right) => 0));
''');
  });

  test('codegen preserves nested binary expression semantics', () {
    const module = EsmModuleIr(
      items: [
        EsmFunctionIr(
          name: 'main',
          export: true,
          parameters: [],
          body: [
            EsmVariableDeclarationIr(
              name: 'value',
              mutable: true,
              initializer: EsmNumberLiteralIr(10),
            ),
            EsmExpressionStatementIr(
              EsmAssignmentIr(
                target: EsmIdentifierIr('value'),
                value: EsmBinaryIr(
                  left: EsmIdentifierIr('value'),
                  operator: '-',
                  right: EsmBinaryIr(
                    left: EsmIdentifierIr('oldPosition'),
                    operator: '-',
                    right: EsmIdentifierIr('newPosition'),
                  ),
                ),
              ),
            ),
          ],
        ),
      ],
    );

    expect(const EsmCodegenStage().emit(module).code, '''
// Generated by dart2esm.

export function main() {
  let value = 10;
  value = value - (oldPosition - newPosition);
}
''');
  });

  test('runtime linker resolves helper declarations before codegen', () {
    const module = EsmModuleIr(
      items: [
        EsmFunctionIr(
          name: 'main',
          export: true,
          parameters: [],
          body: [
            EsmExpressionStatementIr(
              EsmCallIr(
                callee: EsmIdentifierIr('__dartPrint'),
                arguments: [EsmStringLiteralIr('linked')],
              ),
            ),
          ],
        ),
      ],
    );

    final linked = const RuntimeLinkerStage().link(
      _normalizationForModule(
        module,
        runtimeHelpers: const [EsmRuntimeHelper.print],
      ),
    );

    expect(linked.linkedHelpers, [
      EsmRuntimeHelper.print,
      EsmRuntimeHelper.stringify,
    ]);
    expect(linked.module.items, hasLength(3));
    final helper = linked.module.items.first;
    expect(helper, isA<EsmFunctionIr>());
    expect((helper as EsmFunctionIr).name, '__dartPrint');
    expect(helper.export, isFalse);
    expect(const EsmCodegenStage().emit(linked.module).code, '''
// Generated by dart2esm.

function __dartPrint(value) {
  console.log(__dartStr(value));
}

function __dartStr(value) {
  if (value == null) return "null";
  if (Array.isArray(value)) {
    return "[" + value.map(__dartStr).join(", ") + "]";
  }
  if (value instanceof Set) {
    return "{" + Array.from(value).map(__dartStr).join(", ") + "}";
  }
  if (value instanceof Map) {
    return "{" + Array.from(value, ([key, entryValue]) => __dartStr(key) + ": " + __dartStr(entryValue)).join(", ") + "}";
  }
  if (typeof value === "object") {
    const toString = value.toString;
    if (typeof toString === "function" && toString !== Object.prototype.toString) {
      return String(toString.call(value));
    }
  }
  return String(value);
}

export function main() {
  __dartPrint("linked");
}
''');
  });

  test('semantic world reserves pipeline-provided generated global names', () {
    final libraryUri = Uri.parse('package:sample/main.dart');
    final library = k.Library(libraryUri, fileUri: libraryUri);
    final helperCollision = k.Procedure(
      k.Name('__dartPrint', library),
      k.ProcedureKind.Method,
      k.FunctionNode(k.EmptyStatement()),
      fileUri: Uri.parse('memory:__dartPrint.dart'),
      isStatic: true,
    );
    final main = _procedure(
      'main',
      body: k.ExpressionStatement(
        k.StaticInvocation(helperCollision, k.Arguments([])),
      ),
    );
    library.addProcedure(helperCollision);
    library.addProcedure(main);
    final component = k.Component(libraries: [library]);
    component.setMainMethodAndMode(main.reference, true);

    final result = Dart2EsmCompilerPipeline(
      options: const Dart2EsmPipelineOptions(
        runMain: false,
        allowLegacyOracle: false,
      ),
    ).compile(component);

    expect(result.path, Dart2EsmCompilerPath.newCore);
    final collisionSymbol = result.semantic!.world.symbolForRequired(
      helperCollision,
    );
    expect(collisionSymbol.name, '__dartPrint_1');
  });

  test('semantic world folds anonymous mixin superclass edges', () {
    final libraryUri = Uri.parse('package:sample/main.dart');
    final library = k.Library(libraryUri, fileUri: libraryUri);
    final base = k.Class(name: 'Base', fileUri: libraryUri);
    final mixin = k.Class(name: 'Mixin', fileUri: libraryUri);
    final mixinMethod = k.Procedure(
      k.Name('label'),
      k.ProcedureKind.Method,
      k.FunctionNode(k.ReturnStatement(k.StringLiteral('mixin'))),
      fileUri: Uri.parse('memory:mixin_label.dart'),
    );
    final mixinApplication = k.Class(
      name: r'_Derived&Base&Mixin',
      isAnonymousMixin: true,
      supertype: k.Supertype(base, const []),
      mixedInType: k.Supertype(mixin, const []),
      fileUri: libraryUri,
    );
    mixinApplication.addProcedure(mixinMethod);
    final derived = k.Class(
      name: 'Derived',
      supertype: k.Supertype(mixinApplication, const []),
      fileUri: libraryUri,
    );
    final main = _procedure('main', body: k.EmptyStatement());
    library.addClass(base);
    library.addClass(mixin);
    library.addClass(mixinApplication);
    library.addClass(derived);
    library.addProcedure(main);
    final component = k.Component(libraries: [library]);
    component.setMainMethodAndMode(main.reference, true);

    final semantic = const SemanticWorldStage().build(
      KernelFrontendResult(component: component, main: main),
    );

    expect(
      semantic.world.classes.map((klass) => klass.node),
      isNot(contains(mixinApplication)),
    );
    final derivedSymbol = semantic.world.classSymbolFor(derived);
    expect(derivedSymbol, isNotNull);
    expect(derivedSymbol!.jsSuperclass, same(base));
    expect(derivedSymbol.interfaceMarkerClasses, contains(mixin));
    final loweredMixinMethod = semantic.world.instanceProcedureSymbolFor(
      mixinMethod,
    );
    expect(loweredMixinMethod, isNotNull);
    expect(loweredMixinMethod!.name, 'label');
    expect(loweredMixinMethod.emit, isTrue);
    expect(derivedSymbol.procedures, contains(same(loweredMixinMethod)));
  });

  test('compiles switch continue through the new compiler core', () {
    final result = Dart2EsmCompilerPipeline(
      options: const Dart2EsmPipelineOptions(runMain: true),
    ).compile(_componentWithSwitchContinue());

    expect(result.path, Dart2EsmCompilerPath.newCore);
    expect(result.usedLegacyOracle, isFalse);
    expect(result.completedStages, dart2EsmCompilerStageOrder);
    expect(result.code, contains(r'$switchTarget'));
    expect(result.code, contains(r'continue $switchLoop;'));
  });

  test(
    'rejects unsupported Kernel without invoking the legacy oracle by default',
    () {
      final component = _componentWithYieldStatement();

      final pipeline = Dart2EsmCompilerPipeline(
        options: const Dart2EsmPipelineOptions(runMain: true),
      );

      expect(
        () => pipeline.compile(component),
        throwsA(isA<NewCompilerUnsupported>()),
      );
    },
  );

  test('can explicitly invoke the legacy oracle for unsupported Kernel', () {
    final component = _componentWithYieldStatement();

    final result = Dart2EsmCompilerPipeline(
      options: const Dart2EsmPipelineOptions(
        runMain: true,
        allowLegacyOracle: true,
      ),
    ).compile(component);

    expect(result.path, Dart2EsmCompilerPath.legacyOracle);
    expect(result.usedLegacyOracle, isTrue);
    expect(result.legacyOracle?.reason, contains('YieldStatement'));
  });

  test('can reject unsupported Kernel without invoking the legacy oracle', () {
    final component = _componentWithYieldStatement();

    final pipeline = Dart2EsmCompilerPipeline(
      options: const Dart2EsmPipelineOptions(
        runMain: true,
        allowLegacyOracle: false,
      ),
    );

    expect(
      () => pipeline.compile(component),
      throwsA(isA<NewCompilerUnsupported>()),
    );
  });

  test('rejects components without a main method in the frontend stage', () {
    final pipeline = Dart2EsmCompilerPipeline(
      options: const Dart2EsmPipelineOptions(runMain: true),
    );

    expect(
      () => pipeline.compile(k.Component()),
      throwsA(isA<UnsupportedKernelNode>()),
    );
  });
}

k.Component _componentWithSwitchContinue() {
  final libraryUri = Uri.parse('package:sample/main.dart');
  final library = k.Library(libraryUri, fileUri: libraryUri);
  final target = k.SwitchCase(
    [k.IntLiteral(0)],
    [k.TreeNode.noOffset],
    k.EmptyStatement(),
  );
  final main = _procedure(
    'main',
    body: k.Block([
      k.SwitchStatement(k.IntLiteral(0), [
        target,
        k.SwitchCase(
          [k.IntLiteral(1)],
          [k.TreeNode.noOffset],
          k.Block([k.ContinueSwitchStatement(target)]),
        ),
      ]),
    ]),
  );
  library.addProcedure(main);
  final component = k.Component(libraries: [library]);
  component.setMainMethodAndMode(main.reference, true);
  return component;
}

k.Component _componentWithYieldStatement() {
  final libraryUri = Uri.parse('package:sample/main.dart');
  final library = k.Library(libraryUri, fileUri: libraryUri);
  final main = _procedure(
    'main',
    body: k.Block([k.YieldStatement(k.IntLiteral(1))]),
  );
  library.addProcedure(main);
  final component = k.Component(libraries: [library]);
  component.setMainMethodAndMode(main.reference, true);
  return component;
}

k.Procedure _procedure(
  String name, {
  required k.Statement body,
  List<k.VariableDeclaration> positionalParameters = const [],
}) {
  return k.Procedure(
    k.Name(name),
    k.ProcedureKind.Method,
    k.FunctionNode(body, positionalParameters: positionalParameters),
    fileUri: Uri.parse('memory:$name.dart'),
    isStatic: true,
  );
}

NormalizationResult _normalizationForModule(
  EsmModuleIr module, {
  Iterable<EsmRuntimeHelper> runtimeHelpers = const [],
}) {
  final libraryUri = Uri.parse('package:sample/main.dart');
  final library = k.Library(libraryUri, fileUri: libraryUri);
  final main = _procedure('main', body: k.EmptyStatement());
  library.addProcedure(main);
  final component = k.Component(libraries: [library]);
  component.setMainMethodAndMode(main.reference, true);
  final kernel = KernelFrontendResult(component: component, main: main);
  final semantic = SemanticWorldResult(
    kernel: kernel,
    world: EsmSemanticWorld(
      component: component,
      main: main,
      classes: const [],
      extensionTypes: const [],
      fields: const [],
      procedures: [
        EsmProcedureSymbol(
          node: main,
          name: 'main',
          export: true,
          kind: EsmProcedureKind.method,
        ),
      ],
    ),
  );
  return NormalizationResult(
    irBuild: EsmIrBuildResult(
      lowering: LoweringResult(
        semantic: semantic,
        items: module.items,
        runtimeHelpers: runtimeHelpers,
      ),
      module: module,
    ),
    module: module,
    invalidatesSemanticWorld: false,
  );
}
