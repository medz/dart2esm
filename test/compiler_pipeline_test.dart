import 'package:dart2esm/src/compiler_core/codegen/esm_codegen.dart';
import 'package:dart2esm/src/compiler_core/compiler_pipeline.dart';
import 'package:dart2esm/src/compiler_core/frontend/kernel_frontend.dart';
import 'package:dart2esm/src/compiler_core/ir/esm_ir.dart';
import 'package:dart2esm/src/compiler_core/lowering/kernel_to_esm_ir.dart';
import 'package:dart2esm/src/compiler_core/new_compiler_unsupported.dart';
import 'package:dart2esm/src/compiler_core/runtime/runtime_helpers.dart';
import 'package:dart2esm/src/compiler_core/runtime/runtime_linker.dart';
import 'package:dart2esm/src/compiler_core/semantic/semantic_world.dart';
import 'package:dart2esm/src/compiler_core/transform/module_normalizer.dart';
import 'package:dart2esm/src/diagnostics/unsupported_kernel_node.dart';
import 'package:kernel/kernel.dart' as k;
import 'package:test/test.dart';

void main() {
  test('compiles supported Kernel through the new compiler core', () {
    final libraryUri = Uri.parse('package:sample/main.dart');
    final library = k.Library(libraryUri, fileUri: libraryUri);
    final helperParameter = k.VariableDeclaration('value');
    final helper = _procedure(
      'helper',
      body: k.Block([k.ReturnStatement(k.VariableGet(helperParameter))]),
      positionalParameters: [helperParameter],
    );
    final message = k.VariableDeclaration(
      'message',
      initializer: k.StringLiteral('ok'),
      isFinal: true,
    );
    final main = _procedure(
      'main',
      body: k.Block([
        message,
        k.ExpressionStatement(
          k.StaticInvocation(helper, k.Arguments([k.VariableGet(message)])),
        ),
      ]),
    );
    library.addProcedure(helper);
    library.addProcedure(main);
    final component = k.Component(libraries: [library]);
    component.setMainMethodAndMode(main.reference, true);

    final result = Dart2EsmCompilerPipeline(
      options: const Dart2EsmPipelineOptions(runMain: true),
    ).compile(component);

    expect(result.path, Dart2EsmCompilerPath.newCore);
    expect(result.usedLegacyOracle, isFalse);
    expect(result.kernel.component, same(component));
    expect(result.semantic?.world.main, same(main));
    expect(result.lowering?.module.items, hasLength(3));
    expect(result.lowering?.runtimeHelpers, isEmpty);
    expect(result.normalization?.invalidatesSemanticWorld, isFalse);
    expect(result.runtime?.linkedHelpers, isEmpty);
    expect(result.codegen?.code, same(result.code));
    expect(result.code, '''
// Generated by dart2esm.

export function helper(value) {
  return value;
}

export function main() {
  const message = "ok";
  helper(message);
}

main();
''');
  });

  test('runtime linker resolves helper declarations before codegen', () {
    const module = EsmModuleIr(
      items: [
        EsmFunctionIr(
          name: 'main',
          export: true,
          parameters: [],
          body: [
            EsmExpressionStatementIr(
              EsmCallIr(
                callee: EsmIdentifierIr('__dartPrint'),
                arguments: [EsmStringLiteralIr('linked')],
              ),
            ),
          ],
        ),
      ],
    );

    final linked = const RuntimeLinkerStage().link(
      _normalizationForModule(
        module,
        runtimeHelpers: const [EsmRuntimeHelper.print],
      ),
    );

    expect(linked.linkedHelpers, [EsmRuntimeHelper.print]);
    expect(linked.module.items, hasLength(2));
    final helper = linked.module.items.first;
    expect(helper, isA<EsmFunctionIr>());
    expect((helper as EsmFunctionIr).name, '__dartPrint');
    expect(helper.export, isFalse);
    expect(const EsmCodegenStage().emit(linked).code, '''
// Generated by dart2esm.

function __dartPrint(value) {
  console.log(value);
}

export function main() {
  __dartPrint("linked");
}
''');
  });

  test('can reject unsupported Kernel without invoking the legacy oracle', () {
    final libraryUri = Uri.parse('package:sample/main.dart');
    final library = k.Library(libraryUri, fileUri: libraryUri);
    final target = k.SwitchCase(
      [k.IntLiteral(0)],
      [k.TreeNode.noOffset],
      k.EmptyStatement(),
    );
    final main = _procedure(
      'main',
      body: k.Block([
        k.SwitchStatement(k.IntLiteral(0), [
          target,
          k.SwitchCase(
            [k.IntLiteral(1)],
            [k.TreeNode.noOffset],
            k.Block([k.ContinueSwitchStatement(target)]),
          ),
        ]),
      ]),
    );
    library.addProcedure(main);
    final component = k.Component(libraries: [library]);
    component.setMainMethodAndMode(main.reference, true);

    final pipeline = Dart2EsmCompilerPipeline(
      options: const Dart2EsmPipelineOptions(
        runMain: true,
        allowLegacyOracle: false,
      ),
    );

    expect(
      () => pipeline.compile(component),
      throwsA(isA<NewCompilerUnsupported>()),
    );
  });

  test('rejects components without a main method in the frontend stage', () {
    final pipeline = Dart2EsmCompilerPipeline(
      options: const Dart2EsmPipelineOptions(runMain: true),
    );

    expect(
      () => pipeline.compile(k.Component()),
      throwsA(isA<UnsupportedKernelNode>()),
    );
  });
}

k.Procedure _procedure(
  String name, {
  required k.Statement body,
  List<k.VariableDeclaration> positionalParameters = const [],
}) {
  return k.Procedure(
    k.Name(name),
    k.ProcedureKind.Method,
    k.FunctionNode(body, positionalParameters: positionalParameters),
    fileUri: Uri.parse('memory:$name.dart'),
    isStatic: true,
  );
}

NormalizationResult _normalizationForModule(
  EsmModuleIr module, {
  Iterable<EsmRuntimeHelper> runtimeHelpers = const [],
}) {
  final libraryUri = Uri.parse('package:sample/main.dart');
  final library = k.Library(libraryUri, fileUri: libraryUri);
  final main = _procedure('main', body: k.EmptyStatement());
  library.addProcedure(main);
  final component = k.Component(libraries: [library]);
  component.setMainMethodAndMode(main.reference, true);
  final kernel = KernelFrontendResult(component: component, main: main);
  final semantic = SemanticWorldResult(
    kernel: kernel,
    world: EsmSemanticWorld(
      component: component,
      main: main,
      classes: const [],
      fields: const [],
      procedures: [
        EsmProcedureSymbol(
          node: main,
          name: 'main',
          export: true,
          kind: EsmProcedureKind.method,
        ),
      ],
    ),
  );
  return NormalizationResult(
    lowering: LoweringResult(
      semantic: semantic,
      module: module,
      runtimeHelpers: runtimeHelpers,
    ),
    module: module,
    invalidatesSemanticWorld: false,
  );
}
